Rails.application.routes.draw do

  post 'users/user_auth'
  get 'users/logged_in'

  resources :users, only: [:create, :update, :destroy]
  resources :sessions, only: [:create, :destroy]

  namespace :api do
    resources :categories
    resources :products
    resources :carts, only: [:create, :destroy]
  end
  
end
